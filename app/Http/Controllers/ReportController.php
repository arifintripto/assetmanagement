    {
        Report::create(\request()->validate([
            'date' => 'required',
            'asm_rsm' => 'required|integer',
            'report_area' => 'required',
            'report_asm' => 'required',
            'report_territory' => 'required',
            'report_tso' => 'required',
            'report_town' => 'required',
            'report_spo' => 'required',
            'report_db' => 'required'
        ]));

        DB::table('dbpointreview')->updateOrInsert(\request()->validate([
            'db_avg_sale' => '',
            'wh_mgmt' => ''
        ]));

        return view('dashboard.pages.dashboard.index');
    }

